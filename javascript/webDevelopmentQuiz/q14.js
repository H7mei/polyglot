// TTFB - Time to First Byte
// Ini adalah waktu yang dibutuhkan server untuk menanggapi permintaan dan mulai mengirimkan data kembali ke klien. Bayangkan seperti waktu yang dibutuhkan untuk seorang pelayan memulai penyajian pesanan Anda di restoran

// FID - First Input Delay
// Ini adalah waktu yang dibutuhkan sebuah halaman web untuk merespons interaksi pertama pengguna. Ini seperti seberapa cepat sebuah mesin merespons setelah Anda menekan tombol.

// TTI - Time to Interactive
// Ini adalah waktu yang dibutuhkan bagi sebuah halaman web untuk sepenuhnya dimuat dan responsif terhadap interaksi pengguna. Ini seperti waktu yang dibutuhkan untuk sebuah permainan video sepenuhnya siap dimainkan setelah Anda membukanya.

// TBT - Total Blocking Time
// Ini adalah waktu dimana thread utama diblokir dari merespons input pengguna. Bayangkan seperti waktu dimana komputer Anda tidak dapat merespons apa pun karena sedang sibuk.

// CLS - Cumulative Layout Shift
// Ini adalah kestabilan tata letak sebuah halaman web, atau pergeseran elemen yang tak terduga pada halaman yang terjadi saat halaman web dimuat. Seperti ketika Anda membaca artikel dan tiba-tiba teks dan gambar berpindah tempat.

// INP - Input Latency
// Ini adalah waktu rata-rata yang dibutuhkan sebuah halaman web untuk memperbarui tampilan visualnya setelah pengguna berinteraksi dengannya. Ini seperti waktu yang dibutuhkan untuk melihat hasil ketika Anda mengklik sesuatu di layar.
