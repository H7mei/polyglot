/* 

Transport Security di server web, juga dikenal sebagai Transport Layer Security (TLS) atau Secure Sockets Layer (SSL), adalah protokol yang digunakan untuk mengamankan komunikasi antara server web dan klien (seperti browser). Berikut adalah poin utama dari Transport Security:

1. **Enkripsi Data**: TLS mengenkripsi data yang dikirim antara server dan klien. Ini berarti bahwa informasi sensitif seperti kata sandi, informasi kartu kredit, dan data pribadi lainnya dienkripsi sehingga tidak dapat dibaca oleh pihak ketiga yang mungkin menyadap koneksi.

2. **Autentikasi Server**: TLS menggunakan sertifikat digital untuk memastikan bahwa klien sedang berkomunikasi dengan server yang benar. Ini menghindari serangan man-in-the-middle, di mana penyerang mencoba menyamar sebagai server yang sah.

3. **Integritas Data**: Protokol ini juga menyediakan mekanisme untuk memeriksa integritas data yang dikirim, memastikan bahwa data tidak diubah selama transmisi.

4. **Negosiasi Protokol**: Selama proses "handshake" TLS, server dan klien setuju pada detail protokol seperti versi TLS dan metode enkripsi yang akan digunakan.

Implementasi Transport Security di server web sangat penting untuk melindungi data pengguna dan memelihara kepercayaan dalam layanan web, terutama untuk transaksi online dan pertukaran informasi sensitif.

*/
