# include <stdio.h>
# include <stdlib.h>

int main()
{
    int index = 1;
    while (index <= 9){
        printf("%d\n", index);
        index++;
    }
    printf("\n");
    int spnk = 9;
    do {
        printf("%d\n", spnk);
        spnk++;
    }while (spnk <= 9);
    /* yang kedua ini menecek angka dulu baru 
    melakukan pengulangan */

    return 0;
}



/*
ini tentang while loop
merupakan sebuah struktur dalam bahasa C
yang megulang kondisi sampai hal yang di inginkan
pengulangan
*/